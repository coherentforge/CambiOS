/// Built-in 8×8 ASCII font covering 0x20..=0x7F. See module docs for
/// layout and swap strategy.
pub const BUILTIN_FONT_8X8: Font = Font {
    glyph_width: 8,
    glyph_height: 8,
    first: 0x20,
    num_glyphs: 96,
    data: &FONT_8X8_DATA,
};

// ============================================================================
// Rendering — `draw_text` lives here (not on Surface) so the font
// module owns the glyph walk.
// ============================================================================

impl Surface<'_> {
    /// Draw `text` at (x, y) as the top-left pen position, in `color`,
    /// using the supplied font. Each glyph advances the pen by
    /// `font.glyph_width` regardless of whether the glyph is present
    /// (blank placeholders still reserve their cell — avoids layout
    /// jitter when a font is swapped in with richer coverage).
    ///
    /// Clips to the surface bounds. Non-ASCII bytes render as blank
    /// cells. Newline and other control chars are NOT special-cased
    /// in v0 — the caller lays out lines.
    pub fn draw_text(&mut self, x: i32, y: i32, text: &str, color: Color, font: &Font) {
        let mut pen_x = x;
        for ch in text.as_bytes() {
            self.draw_glyph(pen_x, y, *ch, color, font);
            pen_x = pen_x.saturating_add(font.glyph_width as i32);
        }
    }

    /// Draw text using the built-in 8×8 font. Thin wrapper for the
    /// common case.
    pub fn draw_text_builtin(&mut self, x: i32, y: i32, text: &str, color: Color) {
        self.draw_text(x, y, text, color, &BUILTIN_FONT_8X8);
    }

    fn draw_glyph(&mut self, x: i32, y: i32, ch: u8, color: Color, font: &Font) {
        let rows = match font.glyph(ch) {
            Some(r) => r,
            None => return, // blank cell — pen still advanced by caller
        };
        let w = font.glyph_width as i32;
        for (row_i, row) in rows.iter().enumerate() {
            let py = y + row_i as i32;
            let byte = *row;
            for col in 0..w {
                let bit = 7 - col; // bit 7 = leftmost
                if (byte >> bit) & 1 == 1 {
                    self.set_pixel(x + col, py, color);
                }
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn glyph_range() {
        assert!(BUILTIN_FONT_8X8.glyph(b' ').is_some());
        assert!(BUILTIN_FONT_8X8.glyph(b'0').is_some());
        assert!(BUILTIN_FONT_8X8.glyph(b'Z').is_some());
        assert!(BUILTIN_FONT_8X8.glyph(0x1F).is_none()); // below range
        assert!(BUILTIN_FONT_8X8.glyph(0x80).is_none()); // above range
    }

    #[test]
    fn digits_non_blank() {
        // If someone replaces a digit with zeros by mistake, at least
        // the all-zero check catches it.
        for ch in b'0'..=b'9' {
            let g = BUILTIN_FONT_8X8.glyph(ch).unwrap();
            assert!(g.iter().any(|&b| b != 0), "digit '{}' is blank", ch as char);
        }
    }

    #[test]
    fn uppercase_non_blank() {
        for ch in b'A'..=b'Z' {
            let g = BUILTIN_FONT_8X8.glyph(ch).unwrap();
            assert!(g.iter().any(|&b| b != 0), "letter '{}' is blank", ch as char);
        }
    }

    #[test]
    fn text_width_is_len_times_glyph_width() {
        assert_eq!(BUILTIN_FONT_8X8.text_width("HELLO"), 5 * 8);
        assert_eq!(BUILTIN_FONT_8X8.text_width(""), 0);
    }
}
