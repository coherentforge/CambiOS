//! Bitmap font format + built-in 8×8 font.
//!
//! Each glyph is `GLYPH_HEIGHT` consecutive bytes. Within each byte,
//! bit 7 is the leftmost pixel and bit 0 is the rightmost. A set bit
//! draws the caller-chosen color; a clear bit is left untouched
//! (transparent background — the caller pre-fills if it wants
//! opaque text).
//!
//! The built-in font covers ASCII printable range `0x20..=0x7F` with
//! digits, uppercase letters, and common punctuation hand-drawn at
//! 8×8. Lowercase letters and rare punctuation are blank placeholders
//! — the font is explicitly superficial and swappable (see the
//! `user_font` example in `draw_text`). A future pass can replace
//! `BUILTIN_FONT_8X8.data` with e.g. the IBM VGA 8×16 ROM font
//! (public domain) without changing the API.

use crate::surface::Surface;
use crate::Color;

/// Bitmap font definition. Glyphs are stored in row-major order,
/// `glyph_height` bytes per glyph, one byte per row. Bit 7 = leftmost
/// pixel; `glyph_width` must be ≤ 8 (v0 keeps each row in one byte).
///
/// Font covers a contiguous ASCII range `first..first + num_glyphs`;
/// out-of-range characters render as a blank cell advancing the
/// pen (so untranslated lowercase still takes up its slot and
/// doesn't visually collide with neighbours).
#[derive(Clone, Copy)]
pub struct Font {
    pub glyph_width: u8,
    pub glyph_height: u8,
    pub first: u8,
    pub num_glyphs: u8,
    pub data: &'static [u8],
}

impl Font {
    /// Returns the glyph row data for `ch`, or `None` if out of range
    /// / the byte slice is too short.
    pub fn glyph(&self, ch: u8) -> Option<&'static [u8]> {
        if ch < self.first {
            return None;
        }
        let idx = (ch - self.first) as usize;
        if idx >= self.num_glyphs as usize {
            return None;
        }
        let h = self.glyph_height as usize;
        let start = idx * h;
        let end = start + h;
        if end > self.data.len() {
            return None;
        }
        Some(&self.data[start..end])
    }

    /// Width in pixels of an ASCII string rendered in this font.
    /// Ignores wrap / clip — that's the caller's or `draw_text`'s
    /// concern.
    pub fn text_width(&self, s: &str) -> u32 {
        (s.len() as u32) * (self.glyph_width as u32)
    }
}

